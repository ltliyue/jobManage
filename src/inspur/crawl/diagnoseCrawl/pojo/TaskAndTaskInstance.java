package inspur.crawl.diagnoseCrawl.pojo;

public class TaskAndTaskInstance {
	String task_id;//任务id
	String task_name;//任务名称
	String instance_id;//实例id
	String id;//规则id
	String name;//规则名称
	String publish_time;//任务实例publish时间
	public String getPublish_time() {
		return publish_time;
	}
	public void setPublish_time(String publish_time) {
		this.publish_time = publish_time;
	}
	public String getTask_id() {
		return task_id;
	}
	public void setTask_id(String task_id) {
		this.task_id = task_id;
	}
	public String getTask_name() {
		return task_name;
	}
	public void setTask_name(String task_name) {
		this.task_name = task_name;
	}
	public String getInstance_id() {
		return instance_id;
	}
	public void setInstance_id(String instance_id) {
		this.instance_id = instance_id;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	@Override
	public String toString() {
		return "TaskAndTaskInstance [task_id=" + task_id + ", task_name=" + task_name + ", instance_id=" + instance_id
				+ ", id=" + id + ", name=" + name + ", publish_time=" + publish_time + "]";
	}

}
